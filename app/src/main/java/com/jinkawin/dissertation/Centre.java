package com.jinkawin.dissertation;

public class Centre {

    private double x;
    private double y;

    public Centre(int x, int y){
        this.x = x * 1.0;
        this.y = y * 1.0;
    }

    public double getX() {
        return this.x;
    }

    public double getY() {
        return this.y;
    }
}
